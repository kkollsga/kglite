"""Synthetic knowledge-graph generator for the graphsuite benchmark.

A *typical kglite problem*: a heterogeneous org/social knowledge graph
with several node types, several edge types, scalar properties for
filtering/aggregation, dense KNOWS subgraph for multi-hop traversal,
and a DEPENDS_ON DAG for deep variable-length traversal.

The dataset is emitted in a *neutral* shape (lists of dicts + pandas
DataFrames + a frozen set of query parameters). Every backend adapter
consumes the same `Dataset` and runs the same parameterised queries, so
the comparison is apples-to-apples.

Node types
----------
Person   gid, name, age, city, joined_year, active, score
Company  gid, name, industry, size
Project  gid, name, budget, status
Skill    gid, name, category
City     gid, name, population, region

Edge types
----------
KNOWS        Person -> Person   (since_year, weight)   [treated UNDIRECTED for queries]
WORKS_AT     Person -> Company  (role, years)
CONTRIBUTES_TO Person -> Project (hours)
HAS_SKILL    Person -> Skill    (level)
OWNS         Company -> Project
DEPENDS_ON   Project -> Project (DAG, downstream deps) [DIRECTED]
LOCATED_IN   Company -> City

Global ids (`gid`) are contiguous 0..N-1 across *all* node types so the
index-based libraries (rustworkx / igraph) can use them directly; each
node also carries its `type` and a per-type local index.
"""

from __future__ import annotations

from dataclasses import dataclass, field
import json
from pathlib import Path
import random
from typing import Any

import pandas as pd

# Scale presets: number of Person nodes drives everything else.
SCALES: dict[str, dict[str, Any]] = {
    "small": {"persons": 2_000, "knows_per": 6},
    "medium": {"persons": 20_000, "knows_per": 6},
    "large": {"persons": 100_000, "knows_per": 8},
}

# Thresholds for the broadly-runnable workloads (range_filter / degree_filter /
# score_filtered_traversal / bulk_update). Constants — not per-run params — so
# both the in-process `generate()` path and the staged `from_staged()` path
# (which reads params from the core generator's manifest) use identical values
# across every backend, no manifest plumbing required.
SCORE_RANGE = (40.0, 60.0)  # range_filter: Persons with score in [lo, hi]
SCORE_MIN = 50.0  # score_filtered_traversal: KNOWS neighbours with score > this
DEGREE_MIN = 14  # degree_filter: count Persons with undirected KNOWS-degree ≥ this

# Specialized-tier params (Phase 4). EMB_DIM mirrors the core generator's
# Person `embedding` width; GEO_BBOX is the geospatial query window over City
# latitude/longitude; VECTOR_TOPK is the vector-kNN result size.
EMB_DIM = 16
GEO_BBOX = (-20.0, 40.0, -50.0, 50.0)  # (lat_lo, lat_hi, lon_lo, lon_hi)
VECTOR_TOPK = 10

INDUSTRIES = ["tech", "energy", "finance", "health", "retail", "media", "logistics", "gov"]
SKILL_CATEGORIES = ["lang", "framework", "tool", "domain", "soft"]
PROJECT_STATUS = ["planning", "active", "paused", "done", "cancelled"]
REGIONS = ["north", "south", "east", "west", "central"]


@dataclass
class Dataset:
    """Neutral dataset consumed by every adapter."""

    scale: str
    seed: int
    # node tables: type -> list of dicts (each has 'gid' + props + local 'lid')
    nodes: dict[str, list[dict[str, Any]]]
    # edge tables: type -> list of dicts (each has 'src', 'dst' gids + props)
    edges: dict[str, list[dict[str, Any]]]
    # gid ranges per type: type -> (start, end) half-open over global ids
    ranges: dict[str, tuple[int, int]]
    # frozen query parameters so every backend runs identical queries
    params: dict[str, Any]
    # cached pandas frames for bulk loaders (built lazily)
    _frames: dict[str, pd.DataFrame] = field(default_factory=dict)

    @property
    def n_nodes(self) -> int:
        return sum(len(v) for v in self.nodes.values())

    @property
    def n_edges(self) -> int:
        return sum(len(v) for v in self.edges.values())

    def node_frame(self, ntype: str) -> pd.DataFrame:
        key = f"node:{ntype}"
        if key not in self._frames:
            self._frames[key] = pd.DataFrame(self.nodes[ntype])
        return self._frames[key]

    def edge_frame(self, etype: str) -> pd.DataFrame:
        key = f"edge:{etype}"
        if key not in self._frames:
            self._frames[key] = pd.DataFrame(self.edges[etype])
        return self._frames[key]

    def signature(self) -> str:
        """Stable identifier for the dataset shape — runs over the same
        signature are directly comparable."""
        return f"{self.scale}-s{self.seed}-n{self.n_nodes}-e{self.n_edges}"

    @classmethod
    def from_staged(cls, staged: "str | Path", scale: str = "staged") -> "Dataset":
        """Load a dataset that `kglite.graphgen(out=...)` (or the `graphgen`
        CLI) streamed to `staged` — same schema, generated by the bundled
        canonical generator. `pandas.read_csv` types each column, so the node
        dicts match `generate()`'s (gid:int, age:int, score:float, …). The
        query params come from the run's `manifest.json`."""
        import json

        staged = Path(staged)
        manifest = json.loads((staged / "manifest.json").read_text())
        ranges = {k: tuple(v) for k, v in manifest["ranges"].items()}

        nodes: dict[str, list[dict[str, Any]]] = {}
        for ntype in ("Person", "Company", "Project", "Skill", "City"):
            recs = pd.read_csv(staged / f"{ntype}.csv").to_dict("records")
            base = ranges[ntype][0]
            for r in recs:
                r["lid"] = r["gid"] - base
            nodes[ntype] = recs

        edges: dict[str, list[dict[str, Any]]] = {}
        for etype in (
            "KNOWS",
            "WORKS_AT",
            "CONTRIBUTES_TO",
            "HAS_SKILL",
            "OWNS",
            "DEPENDS_ON",
            "LOCATED_IN",
        ):
            edges[etype] = pd.read_csv(staged / f"{etype}.csv").to_dict("records")

        return cls(
            scale=scale,
            seed=int(manifest["seed"]),
            nodes=nodes,
            edges=edges,
            ranges=ranges,
            params=manifest["params"],
        )


def generate(scale: str = "medium", seed: int = 1234) -> Dataset:
    if scale not in SCALES:
        raise ValueError(f"unknown scale {scale!r}; choose from {list(SCALES)}")
    cfg = SCALES[scale]
    rng = random.Random(seed)

    n_person = cfg["persons"]
    knows_per = cfg["knows_per"]
    n_company = max(8, n_person // 25)
    n_project = max(20, n_person // 5)
    n_skill = max(20, n_person // 60)
    n_city = max(10, n_person // 100)

    # ---- allocate contiguous global ids -------------------------------
    cursor = 0

    def alloc(n: int) -> tuple[int, int]:
        nonlocal cursor
        start = cursor
        cursor += n
        return start, start + n

    p0, p1 = alloc(n_person)
    c0, c1 = alloc(n_company)
    pr0, pr1 = alloc(n_project)
    s0, s1 = alloc(n_skill)
    ci0, ci1 = alloc(n_city)
    ranges = {
        "Person": (p0, p1),
        "Company": (c0, c1),
        "Project": (pr0, pr1),
        "Skill": (s0, s1),
        "City": (ci0, ci1),
    }

    # ---- nodes --------------------------------------------------------
    cities = [
        {
            "gid": ci0 + i,
            "lid": i,
            "name": f"City_{i}",
            "population": rng.randint(5_000, 5_000_000),
            "region": REGIONS[i % len(REGIONS)],
            "latitude": round(rng.uniform(-60.0, 70.0), 5),
            "longitude": round(rng.uniform(-170.0, 170.0), 5),
        }
        for i in range(n_city)
    ]
    skills = [
        {
            "gid": s0 + i,
            "lid": i,
            "name": f"Skill_{i}",
            "category": SKILL_CATEGORIES[i % len(SKILL_CATEGORIES)],
        }
        for i in range(n_skill)
    ]
    companies = [
        {
            "gid": c0 + i,
            "lid": i,
            "name": f"Company_{i}",
            "industry": INDUSTRIES[rng.randrange(len(INDUSTRIES))],
            "size": rng.randint(5, 50_000),
        }
        for i in range(n_company)
    ]
    projects = [
        {
            "gid": pr0 + i,
            "lid": i,
            "name": f"Project_{i}",
            "budget": round(rng.uniform(10_000, 5_000_000), 2),
            "status": PROJECT_STATUS[rng.randrange(len(PROJECT_STATUS))],
        }
        for i in range(n_project)
    ]
    persons = [
        {
            "gid": p0 + i,
            "lid": i,
            "name": f"Person_{i}",
            "age": rng.randint(18, 79),
            "city": f"City_{rng.randrange(n_city)}",
            "joined_year": rng.randint(2000, 2025),
            "active": rng.random() < 0.7,
            "score": round(rng.uniform(0.0, 100.0), 3),
            # JSON string to match the core generator's CSV column exactly, so
            # the kglite vector adapter parses it identically in both paths.
            "embedding": json.dumps([round(rng.uniform(-1.0, 1.0), 4) for _ in range(EMB_DIM)]),
        }
        for i in range(n_person)
    ]

    nodes = {
        "Person": persons,
        "Company": companies,
        "Project": projects,
        "Skill": skills,
        "City": cities,
    }

    # ---- edges --------------------------------------------------------
    # KNOWS: a directed social graph; ~knows_per out-edges/person to a
    # nearby-id band (small-world-ish) + some long-range links.
    knows: list[dict[str, Any]] = []
    for i in range(n_person):
        src = p0 + i
        deg = knows_per + (rng.randrange(5) - 2)  # jitter
        for _ in range(max(1, deg)):
            if rng.random() < 0.8:
                j = (i + rng.randint(1, 50)) % n_person  # local band
            else:
                j = rng.randrange(n_person)  # long-range
            if j == i:
                continue
            knows.append(
                {
                    "src": src,
                    "dst": p0 + j,
                    "since_year": rng.randint(2000, 2025),
                    "weight": round(rng.uniform(0.1, 1.0), 3),
                }
            )

    works_at = []
    for i in range(n_person):
        works_at.append(
            {
                "src": p0 + i,
                "dst": c0 + rng.randrange(n_company),
                "role": rng.choice(["eng", "mgr", "sales", "ops", "exec"]),
                "years": rng.randint(0, 25),
            }
        )

    contributes = []
    for i in range(n_person):
        for _ in range(rng.randint(1, 3)):
            contributes.append(
                {
                    "src": p0 + i,
                    "dst": pr0 + rng.randrange(n_project),
                    "hours": rng.randint(1, 2000),
                }
            )

    has_skill = []
    for i in range(n_person):
        chosen = rng.sample(range(n_skill), k=min(n_skill, rng.randint(2, 6)))
        for sk in chosen:
            has_skill.append(
                {
                    "src": p0 + i,
                    "dst": s0 + sk,
                    "level": rng.randint(1, 5),
                }
            )

    owns = []
    for i in range(n_project):
        owns.append({"src": c0 + rng.randrange(n_company), "dst": pr0 + i})

    # DEPENDS_ON: a DAG — project i may depend on lower-indexed projects.
    depends = []
    for i in range(n_project):
        if i == 0:
            continue
        for _ in range(rng.randint(0, 3)):
            j = rng.randrange(i)  # strictly lower index -> acyclic
            depends.append({"src": pr0 + i, "dst": pr0 + j})

    located_in = [{"src": c0 + i, "dst": ci0 + rng.randrange(n_city)} for i in range(n_company)]

    edges = {
        "KNOWS": knows,
        "WORKS_AT": works_at,
        "CONTRIBUTES_TO": contributes,
        "HAS_SKILL": has_skill,
        "OWNS": owns,
        "DEPENDS_ON": depends,
        "LOCATED_IN": located_in,
    }

    # ---- dedupe to a SIMPLE graph -------------------------------------
    # Drop self-loops and parallel edges so degree / neighbourhood
    # semantics are unambiguous and identical across every backend
    # (a simple Graph collapses parallel edges; a multigraph counts them —
    # removing parallels eliminates that source of cross-engine divergence).
    # KNOWS is undirected → dedupe on the unordered pair; the rest are
    # directed → dedupe on the ordered pair.
    def _dedupe(rows: list[dict[str, Any]], undirected: bool) -> list[dict[str, Any]]:
        seen: set = set()
        out = []
        for r in rows:
            s, d = r["src"], r["dst"]
            if s == d:
                continue
            key = frozenset((s, d)) if undirected else (s, d)
            if key in seen:
                continue
            seen.add(key)
            out.append(r)
        return out

    for et in edges:
        edges[et] = _dedupe(edges[et], undirected=(et == "KNOWS"))

    # ---- frozen query parameters --------------------------------------
    pgids = list(range(p0, p1))
    prgids = list(range(pr0, pr1))
    params = {
        "person_range": (p0, p1),
        "company_range": (c0, c1),
        "project_range": (pr0, pr1),
        "filter_city": f"City_{n_city // 2}",
        "filter_age": 30,
        "lookup_ids": rng.sample(pgids, k=min(500, len(pgids))),
        # progressively smaller seed sets for deeper hops keep variable-length
        # path expansion tractable at scale (the workload stays comparable
        # across libraries because every library uses the same seeds per group).
        "seed_persons": rng.sample(pgids, k=min(200, len(pgids))),
        "seed_persons_small": rng.sample(pgids, k=min(50, len(pgids))),
        "seed_persons_tiny": rng.sample(pgids, k=min(20, len(pgids))),
        "seed_projects": rng.sample(prgids, k=min(100, len(prgids))),
        "sp_pairs": [(rng.choice(pgids), rng.choice(pgids)) for _ in range(100)],
        "topk": 20,
        # mutation workload params (new ids start above the global cursor)
        "mut_new_base": cursor,
        "mut_new_count": 500,
        "mut_update_ids": rng.sample(pgids, k=min(500, len(pgids))),
        "mut_delete_ids": rng.sample(pgids, k=min(200, len(pgids))),
    }

    return Dataset(scale=scale, seed=seed, nodes=nodes, edges=edges, ranges=ranges, params=params)
